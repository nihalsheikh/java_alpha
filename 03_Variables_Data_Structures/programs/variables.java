public class variables {
    public static void main(String args[]) {
        // integer variable
        int a = 5;
        int b = 10;
        System.out.println(a);
        System.out.println(b);

        // string vaiable
        String myName = "Nihal Sheikh";
        System.out.println(myName);

        // changing the value of a variable
        a = 20;
        System.out.println(a);
    }
}
